def penambahan(a,b):
	total = a+b
	return (total)

def perkalian(a,b):
	total = a*b
	return (total)

def pengurangan(a,b):
	totalPengurangan = a-b
	return (totalPengurangan)

def kuadrat(a):
	total = a**2
	return (total)

