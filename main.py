def penambahan(a,b):
	total = a+b
	return (total)

def perkalian(a,b):
	total = a*b
	return (total)

def pengurangan(a,b):
	totalPengurangan = a-b
	return (totalPengurangan)

def pangkat(a,b):
	totalPangkat = a**b
	return (totalPangkat)

def main():
	print (penambahan(5,10))
	print (perkalian(6,10))
	print (pengurangan(10,5))
	print (pangkat(2,16))

main()
